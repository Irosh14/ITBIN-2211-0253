    private void sBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sBtnActionPerformed
        // TODO add your handling code here:
        String BookId = seTF.getText();
        
        try {
           
            String bkID = null;
            String bkName = null;
            String author = null;
            String cate = null;
            
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            stmt = DBConnection.getStatementConnection();
            
            rs = stmt.executeQuery("SELECT * FROM book WHERE id = '"+BookId+"'");
            
          while(rs.next()){
          
          bkID= rs.getString("id");
          bkName= rs.getString("bname");
          author= rs.getString("author");
         cate= rs.getString("catagory");
         
         
          
          
          }
          bIDTF.setText(bkID);
           bkNameTF.setText(bkName);
            authorTF.setText(author);
            
           
            
         DBConnection.closecon();
        } catch (Exception e) {
            
        }
    }//GEN-LAST:event_sBtnActionPerformed

