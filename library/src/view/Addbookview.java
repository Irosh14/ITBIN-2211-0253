    private void adbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adbtnActionPerformed
        // TODO add your handling code here:
        if(evt.getSource() == adbtn){
        
        String bookID = bIDTF.getText();
        String bookName = bkNameTF.getText();
        String Author = authorTF.getText();
        String category = cgTF.getSelectedItem().toString();
        
        if(bookID.equals("")){
        
        JOptionPane.showMessageDialog(null, "Enter Book ID","Error",JOptionPane.ERROR_MESSAGE);
        
        }
        
        if(bookName.equals("")){
        
        JOptionPane.showMessageDialog(null, "Enter Book Name","Error",JOptionPane.ERROR_MESSAGE);
        
        }
        if(Author.equals("")){
        
        JOptionPane.showMessageDialog(null, "Enter Author","Error",JOptionPane.ERROR_MESSAGE);
        
        }
        if(category.equals("")){
        
        JOptionPane.showMessageDialog(null, "Enter category","Error",JOptionPane.ERROR_MESSAGE);
        
        }else{
        
            Insertcontroller.insertForm(bookID, bookName, Author, category);
        
        }
        bIDTF.setText("");
        bkNameTF.setText("");
        authorTF.setText("");
        
        
        }
        tableload();
        
    }//GEN-LAST:event_adbtnActionPerformed

