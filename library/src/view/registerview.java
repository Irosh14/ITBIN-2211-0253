    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        
         if(evt.getSource() == submit){
        
        String userID = idTF.getText();
        String userName = nameTF.getText();
        String contact = cnTF1.getText();
        String adderess = AddressTF.getText();
        String password = pass.getText();
        
        if(userID.equals("")){
        
        JOptionPane.showMessageDialog(null, "Enter ID","Error",JOptionPane.ERROR_MESSAGE);
        
        }
        
        if(userName.equals("")){
        
        JOptionPane.showMessageDialog(null, "Enter  Name","Error",JOptionPane.ERROR_MESSAGE);
        
        }
        if(contact.equals("")){
        
        JOptionPane.showMessageDialog(null, "Enter contact","Error",JOptionPane.ERROR_MESSAGE);
        
        }
        if(adderess.equals("")){
        
        JOptionPane.showMessageDialog(null, "Enter Adderess","Error",JOptionPane.ERROR_MESSAGE);
        
        }
        if(password.equals("")){
        
        JOptionPane.showMessageDialog(null, "Enter Password","Error",JOptionPane.ERROR_MESSAGE);
        
        
        }else{
        
            Insertcontroller.insertUser(userID, userName, contact, adderess, password);
        
        }
         }
         idTF.setText("");
         nameTF.setText("");
         cnTF1.setText("");
         AddressTF.setText("");
         pass.setText("");
         
         
         
         tableload();
        
    }//GEN-LAST:event_submitActionPerformed

